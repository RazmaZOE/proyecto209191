var mongoose = require("mongoose");
mongoose.Promise = global.Promise;
var Schema = mongoose.Schema;

var tipoContribuyenteEmpresaSchema = new Schema({
    codigoTA : { type: String, required: true },
    codigoTC : { type: String, required: true },
    tituloTC : { type: String }
});

module.exports = mongoose.model("TipoContribuyenteEmpresa", tipoContribuyenteEmpresaSchema);